//Logical Operators
public class Video23 {
    public static void main(String[] args) {
        System.out.println("LOGICAL AND!!");
        System.out.println((10 > 5) && (2 > 1));
        System.out.println((10 > 5) && (2 < 1));
        System.out.println((10 < 5) && (2 < 1));

        System.out.println("LOGICAL OR!!");
        System.out.println((10 > 5) || (2 > 1));
        System.out.println((10 > 5) || (2 < 1));
        System.out.println((10 < 5) || (2 < 1));

        System.out.println("LOGICAL NOT!!");
        System.out.println(!(10 > 5));
        System.out.println(!(10 < 5));
    }
}
